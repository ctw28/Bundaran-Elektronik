    private ArrayList<String> getSelectedProduct(String barcode) {
        ArrayList<String> temp = new ArrayList<>();

        for (ArrayList<String> string : dataBarang) {
            if (string.get(0).equals(barcode)) {
                idBacode = barcode;
                temp = string;
                return temp;
            }
        }

        return temp;
    }

