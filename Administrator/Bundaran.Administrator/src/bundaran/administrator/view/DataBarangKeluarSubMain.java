    @Override
    public void processDone(String resultMessage) throws SQLException {

        if (resultMessage.length() >= 12) {

            dataSelected = getSelectedProduct(resultMessage.substring(0, 12));
            if (!dataSelected.isEmpty()) {
                productName.setText(dataSelected.get(1));
                inventorySite.setSelectedItem(dataSelected.get(8));
            }
        }

    }

