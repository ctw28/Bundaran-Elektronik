    private ArrayList<ArrayList<String>> searching(){
        ArrayList<ArrayList<String>> temp = new ArrayList<>();
        
        String key = boxPencarian.getText();
        
        if (dataOriginal == null) {
            return null;
        }
        
        for (ArrayList<String> arrayList : dataOriginal) {
            if (arrayList.get(1).toLowerCase().contains(key.toLowerCase())) {
                temp.add(arrayList);
            }
        }
        
        return temp;
    }
