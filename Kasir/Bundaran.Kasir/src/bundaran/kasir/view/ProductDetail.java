    void TotalEvent() {
        NumberValidation validation = new NumberValidation(",", null);
        total.setText(validation.fieldChecked(total.getText()));
        NumberValidation valid = new NumberValidation(",", "Rp ");
        if(diskon.getText().equals("Rp ")){
            diskon.setText("Rp ");
            setCostTotal(total.getText().replace(",", ""), "0");
        }
        else{
            diskon.setText(valid.fieldChecked(diskon.getText()));
            setCostTotal(total.getText().replace(",", ""), diskon.getText().replace("Rp ", "").replace(",", ""));
        }
    }

    /**
     * Menghitung biaya untuk pembelian tiap barang
     *
     * @param jumlah
     */
    void setCostTotal(String jumlah, String diskon) {
        try {
            BigInteger spinnerValue = BigInteger.valueOf(Long.parseLong(jumlah));
            BigInteger diskonValue = BigInteger.valueOf(Long.parseLong(diskon));
            long price = (!dataSelected.get(7).isEmpty()) ? (Long.parseLong(dataSelected.get(3)) / Long.parseLong(dataSelected.get(7)))
                    : Long.parseLong(dataSelected.get(3));
            BigInteger costTotal = (spinnerValue.multiply(BigInteger.valueOf(price))).subtract(diskonValue);
            NumberValidation nv = new NumberValidation(",", "Rp ");
            cost.setText(nv.fieldChecked(String.valueOf(costTotal)));
        } catch (Exception e) {
            NumberValidation nv = new NumberValidation(",", "Rp ");
            cost.setText(nv.fieldChecked(textIdentifier[2]));
        }
    }

