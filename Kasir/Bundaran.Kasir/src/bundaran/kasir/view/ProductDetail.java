    public void sendDetail(KeyEvent evt) {
        Kasir.isDetailShow = false;
        
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_ENTER:
                if (total.getText().isEmpty() || total.getText().equals("0")) {
                    break;
                }

                String price = cost.getText().replace("Rp ", "").replace(",", "");
                String diskon = (this.diskon.getText().replace("Rp ", "").replace(",", "").equals("")) ? "0":this.diskon.getText().replace("Rp ", "").replace(",", "");
                instance.catchUpDetail(add, barcode.getText(), barcodeName.getText(),
                        total.getText(), 
                        String.valueOf(Long.parseLong(dataSelected.get(3))), diskon, price);
                dispose();
                break;
            case KeyEvent.VK_ESCAPE:
                JTextField field = (JTextField) instance.jComboBox1.getEditor().getEditorComponent();
                field.setText("");
                instance.setToTable();
                instance.jComboBox1.requestFocus();
                dispose();
                break;
            default:

                break;
        }
    }

    private ArrayList<String> findProduct(String id) {
        boolean find = false;

        int index = 0;
        for (int i = 0; i < dataBarang.size(); i++) {
            if (id.toLowerCase().equals(dataBarang.get(i)
                    /* i adalah urutan untuk nomor barcode barang*/.get(0).toLowerCase())) {
                find = true;
                index = i;
                break;
            }
        }

        if (find) {
            //labelBarcode.setText("ketemu");
            //empty.setVisible(false);
            return dataBarang.get(index);
        }

        //empty.setText("Tidak ada data yang ditemukan");
        panelDetail.setVisible(false);
        return null;
    }

