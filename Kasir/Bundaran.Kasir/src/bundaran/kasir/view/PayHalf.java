    void sincronDiscount() {
        if (discount.getText().isEmpty()) {
            costTotal.setText(totalHarga);
            return;
        }

        if (discount.getText().equals("0")) {
            costTotal.setText(totalHarga);
            return;
        }

        double getDisount = Double.parseDouble(discount.getText());
        double cost = Double.parseDouble(totalHarga.replace("Rp ", "").replace(",", ""));
        getDisount = (getDisount * cost) / 100;
        cost = cost - getDisount;

        NumberValidation nv = new NumberValidation(",", "Rp ");
        costTotal.setText(nv.fieldChecked(String.valueOf(cost)));
    }

