    private void getCashBack() {
        if (this.bayar.getText().isEmpty()
                || this.bayar.getText().replace("Rp ", "").replace(",", "").isEmpty()) {
            return;
        }

        int total = Integer.parseInt(costTotal.getText().replace("Rp ", "").replace(",", ""));
        int pay = Integer.parseInt(this.bayar.getText().replace("Rp ", "").replace(",", ""));

        if (pay >= total) {
            NumberValidation nv = new NumberValidation(",", "Rp ");
            int back = pay - total;
            kembalian.setText(nv.fieldChecked(String.valueOf(back)));
        } else {
            kembalian.setText("Rp 0");
        }

    }

